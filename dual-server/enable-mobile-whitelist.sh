#!/usr/bin/env bash
# В старой REALITY-схеме этот скрипт добавлял отдельный вход для мобильных
# белых списков. В Hysteria 2 используется UDP-вход и obfs, поэтому отдельный
# REALITY-whitelist больше не применяется.

set -euo pipefail

cat << 'EOF'
Hysteria 2 версия проекта больше не использует отдельный REALITY-вход для
мобильных белых списков.

Что проверить вместо этого:
  1. В панели хостинга открыт UDP-порт Hysteria 2 (обычно 443).
  2. В UFW открыт тот же UDP-порт.
  3. В клиент импортирована свежая hysteria2:// ссылка.

Клиентские параметры лежат на сервере здесь:
  /etc/hysteria/hysteria-client-params.json
EOF
